const Lang = {
  current: localStorage.getItem('lang') || 'es',
 
  get: function(key) {
    const keys = key.split('.');
    let val = i18n[this.current];
    keys.forEach(k => val = val && val[k]);
    return val !== undefined ? val : key;
  },
 
  toggle: function() {
    this.current = this.current === 'en' ? 'es' : 'en';
    localStorage.setItem('lang', this.current);
    window.location.reload();
  },
 
  // Apply all translations via data-i18n attributes
  apply: function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.get(key);
      if (!val || val === key) return;
      if (el.tagName === 'INPUT') {
        el.placeholder = val;
      } else if (el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    });
 
    const btn = document.getElementById('langBtn');
    if (btn) btn.textContent = this.current === 'en' ? 'ES' : 'EN';
    document.documentElement.lang = this.current;
  }
};
 
document.addEventListener('DOMContentLoaded', () => Lang.apply());
 